    private void ButInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButInserirActionPerformed
        String nome = TxtNome.getText();
        String email = TxtEmail.getText();
        String susPlan = "";
        String especialidade = CmBoxEspecialidade.getSelectedItem().toString();
        String plano = TxtNomePlano.getText();
        
        if (ButGConsulta.getSelection().getMnemonic() == 'S'){
            susPlan = "SUS";
        }
        
        if (ButGConsulta.getSelection().getMnemonic() == 'P'){
            susPlan = "Plano de Saúde";
        }
        
        inserirTabela(nome,email,susPlan,especialidade,plano);
        
    }//GEN-LAST:event_ButInserirActionPerformed

    private void MnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MnExcluirActionPerformed
        int linha = TbResumo.getSelectedRow();
        if(linha >= 0){
            ((DefaultTableModel) TbResumo.getModel()).removeRow(linha);
        }
    }//GEN-LAST:event_MnExcluirActionPerformed

    private void SpiHoraIniStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_SpiHoraIniStateChanged
        int spinIni = Integer.parseInt(SpiHoraIni.getValue().toString());

        if(spinIni > 10){
            if(CmBoxEspecialidade.getSelectedItem() == "Ortopedista"){
                SpiHoraFim.setValue(Integer.parseInt(SpiHoraFim.getValue().toString()) + 1);
            }
            else if(CmBoxEspecialidade.getSelectedItem() == "Neurologista"){
                SpiHoraFim.setValue(Integer.parseInt(SpiHoraFim.getValue().toString()) + 2); 
            }
            else if(CmBoxEspecialidade.getSelectedItem() == "Cirurgia Cardíaca"){
                SpiHoraFim.setValue(Integer.parseInt(SpiHoraFim.getValue().toString()) + 4);
            }
            else if(CmBoxEspecialidade.getSelectedItem() == "Clínico Geral"){
                SpiHoraFim.setValue(Integer.parseInt(SpiHoraFim.getValue().toString()) + 1);
            }
            else if(CmBoxEspecialidade.getSelectedItem() == "Pediatra"){
                SpiHoraFim.setValue(Integer.parseInt(SpiHoraFim.getValue().toString()) + 1); 
            }
            else if(CmBoxEspecialidade.getSelectedItem() == "Psicólogo"){
                SpiHoraFim.setValue(Integer.parseInt(SpiHoraFim.getValue().toString()) + 2);
            }

        }

        SpiHoraIni.setValue(spinIni);
    }//GEN-LAST:event_SpiHoraIniStateChanged

    private void SpiHoraFimStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_SpiHoraFimStateChanged
        int spinFim = Integer.parseInt(SpiHoraFim.getValue().toString()); 
        
        SpiHoraFim.setValue(spinFim);  
    }//GEN-LAST:event_SpiHoraFimStateChanged
    private void inserirTabela (String nome, String email, String plano, String especialidade, String nomePlan){
        ((DefaultTableModel) TbResumo.getModel()).addRow(new Object[0]);
        
        int col = 0;
        int linha = TbResumo.getRowCount() - 1;
        
        TbResumo.setValueAt(nome, linha, col++);
        TbResumo.setValueAt(email, linha, col++);
        TbResumo.setValueAt(plano, linha, col++);
        TbResumo.setValueAt(especialidade, linha, col++);
        TbResumo.setValueAt(nomePlan, linha, col++);
    }
