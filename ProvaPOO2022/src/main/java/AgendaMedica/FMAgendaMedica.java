    private void RButSusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RButSusActionPerformed
        if(RButSus.isSelected()){
            PnPlano.setVisible(false);
            
            SpiHoraIni.setValue(10);
            SpiHoraFim.setValue(11);
            
            CmBoxEspecialidade.removeAllItems();
            CmBoxEspecialidade.addItem("Clínico Geral");
            CmBoxEspecialidade.addItem("Psicólogo");
            CmBoxEspecialidade.addItem("Pediatra");
        }
        else if(RButPlano.isSelected()){
            PnPlano.setVisible(true);
            
            SpiHoraIni.setValue(10);
            SpiHoraFim.setValue(11);
            
            CmBoxEspecialidade.removeAllItems();
            CmBoxEspecialidade.addItem("Ortopedista");
            CmBoxEspecialidade.addItem("Neurologista");
            CmBoxEspecialidade.addItem("Cirurgia Cardíaca");
        }
    }//GEN-LAST:event_RButSusActionPerformed

